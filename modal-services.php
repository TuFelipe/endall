                baseado em ondas de ultrassom, para detecção interna de defeitos em materiais
                ou para a medição de espessura de paredes e detecção de corrosão.<br>
                Com o ensaio de ultrassom, uma onda de som ultra-sônica pulso-ecoante
                é enviada através do material.<br>Esta onda será interrompida e então parcialmente
                devolvida, de pontos com imperfeição interna ou da parte posterior da parede do
                material – respectivamente, capturar e analisar o retorno do som junto a informação
                útil coletada, para fornecer informação sobre a localização de imperfeição e a
                espessura da parede do material.
